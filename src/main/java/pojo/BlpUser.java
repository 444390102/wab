package pojo;


public class BlpUser {

  private long uId;
  private String uAcc;
  private String uPwd;
  private long uArea;
  private long rId;


  public long getUId() {
    return uId;
  }

  public void setUId(long uId) {
    this.uId = uId;
  }


  public String getUAcc() {
    return uAcc;
  }

  public void setUAcc(String uAcc) {
    this.uAcc = uAcc;
  }


  public String getUPwd() {
    return uPwd;
  }

  public void setUPwd(String uPwd) {
    this.uPwd = uPwd;
  }


  public long getUArea() {
    return uArea;
  }

  public void setUArea(long uArea) {
    this.uArea = uArea;
  }


  public long getRId() {
    return rId;
  }

  public void setRId(long rId) {
    this.rId = rId;
  }

}
